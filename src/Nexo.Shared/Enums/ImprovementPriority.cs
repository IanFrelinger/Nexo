        /// <summary>
        /// Represents a medium level of priority for improvements.
        /// </summary>
        Medium = 1,

        /// <summary>
        /// Represents a high level of priority for improvements.
        /// </summary>
        High = 2,

        /// <summary>
        /// Represents the highest level of priority for improvements, indicating critical urgency.
        /// </summary>
        Critical = 3
    }
}