namespace Nexo.Core.Application.Enums
{
    /// <summary>
    /// Represents the highest level of template complexity, requiring advanced knowledge and skills to implement.
    /// </summary>
    public enum TemplateComplexity
    {
        /// <summary>
        /// Represents the simplest level of complexity for a template.
        /// </summary>
        Simple = 0,

        /// <summary>
        /// Represents a moderate level of complexity for a template.
        /// </summary>
        Moderate = 1,

        /// <summary>
        /// Represents a complex level of template complexity, requiring substantial effort and expertise to implement.
        /// </summary>
        Complex = 2,

        /// <summary>
        /// Represents the most advanced level of template complexity, suitable for scenarios requiring expert skills and comprehensive understanding.
        /// </summary>
        Advanced = 3
    }
}